local has_node_modules = require('nxtaar.utils.node_js').has_node_modules

local function get_current_location()
    -- If we have a file open, use its parent dir as the search dir.
    -- Otherwise, use the current working directory.
    local dir = vim.fn.getcwd()
    if vim.bo.buftype == '' then
        local bufname = vim.api.nvim_buf_get_name(0)
        if bufname ~= '' then
            dir = vim.fn.fnamemodify(bufname, ':p:h')
        end
    end
    return dir
end

return {
    name = 'reinstall_node_modules',
    desc = 'Fresh installment of the node_modules folder',
    builder = function()
        local tasks = { 'yarn_install' }
        local location = get_current_location()

        if has_node_modules(location) then
            table.insert(tasks, 1, 'remove_node_modules')
        end

        return {
            name = 'Reinstalling node_modules',
            strategy = {
                'orchestrator',
                tasks = tasks,
            },
            components = {
                {
                    'on_complete_dispose',
                    statuses = { 'SUCCESS' },
                    timeout = 1,
                },
                'default',
            },
        }
    end,
}
