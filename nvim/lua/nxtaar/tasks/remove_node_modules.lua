local has_node_modules = require('nxtaar.utils.node_js').has_node_modules

return {
    -- Required fields
    name = 'remove_node_modules',
    desc = 'Remove node_modules folder',
    builder = function()
        -- This must return an overseer.TaskDefinition
        return {
            -- cmd is the only required field
            name = 'Removing node_modules',
            cmd = 'rm',
            -- additional arguments for the cmd
            args = { '-rf', 'node_modules' },
        }
    end,
    priority = 50,
    condition = {
        callback = function(search)
            return has_node_modules(search.dir)
        end,
    },
}
