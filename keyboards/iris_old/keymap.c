void keyboard_post_init_user(void) {
    rgblight_layers = MY_LIGHT_LAYERS;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    rgblight_set_layer_state(0, layer_state_cmp(state, _QWERTY));
    rgblight_set_layer_state(1, layer_state_cmp(state, _RIGHT_M));
    return state;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    os_variant_t os = detected_host_os();

    if (keycode == CHANGE_LANG && record->event.pressed) {
        uprintf("OS: %u\n", os);
        return false;
    }

    switch (keycode) {
        case STORE_SETUPS:
            if (record->event.pressed) {
                store_setups_in_eeprom();
            }
            return false;
        case PRINT_SETUPS:
            if (record->event.pressed) {
                uprintf("OS: %u\n", os);
                print_stored_setups();
                print("--------\n");
            }
            return false;
        case LOG_RGB:
            if (record->event.pressed) {
                uprintf("Color HSV: (%u, %u, %u)\n", rgblight_get_hue(), rgblight_get_sat(), rgblight_get_val());
            }
            return false;
    }

    return true;
};
